import AppRendererControllerBasic from "../../renderer" ;
/*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
创建一个渲染器实例
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
class Renderer extends AppRendererControllerBasic {
    constructor(req, res, next) {
        super(req, res, next) ;
        this.renders() ;
    }
    /*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
    渲染页面
    -----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
    renders() {
        /*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
        计算最近一个月时间，来给查询条件赋予默认值
        -----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
        let now = new Date() ;
        let nowMonth = ( now.getMonth() + 1 ) > 9 ? now.getMonth() + 1 : "0" + (now.getMonth() + 1) ;
        let nowDate = now.getDate() > 9 ? now.getDate() : "0" + now.getDate() ;
        let oneMonthBefore = new Date( now.getTime() - 30 * 24 * 3600 * 1000 ) ;
        let oneMonthBeforeMonth = ( oneMonthBefore.getMonth() + 1 ) > 9 ? oneMonthBefore.getMonth() + 1 : "0" + (oneMonthBefore.getMonth() + 1) ;
        let oneMonthBeforeDate = oneMonthBefore.getDate() > 9 ? oneMonthBefore.getDate() : "0" + oneMonthBefore.getDate() ;
        let defaultDate = {
            "start" : now.getFullYear().toString() + nowMonth.toString() + nowDate.toString() ,
            "end" : oneMonthBefore.getFullYear().toString() + oneMonthBeforeMonth.toString() + oneMonthBeforeDate.toString()
        } ;
        /*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
        获取应用清单数据
        -----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
        /*let apps = await adf.request({
            "apiPath" : "common.apps"
        }) ;
        Object.assign(this.templateData , { "apps" : apps.data }) ; */
          let qs = {
            "type" : this.req.query.type || '',
            "pageId":this.req.query.pageId || '',
            "clickId":this.req.query.clickId || '',
            "firstTime" : this.req.query.firstTime || defaultDate.end ,
            "lastTime" : this.req.query.lastTime || defaultDate.start
        } ;
         /*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
        给查询条件赋值
        -----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
        Object.assign(this.templateData , qs ) ; 
        /*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
        扩展模板数据
        -----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
        Object.assign(this.templateData, { "title" : "自定义详情页" , extraStylesheets : [ ] , "extraJavascripts" : [] }) ;
        /*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
        渲染模板
        -----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/        
        this.render("rent/detail") ; 
    }
}
/*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
最后将render暴露出去
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
export default Renderer ;