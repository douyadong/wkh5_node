import AppRendererControllerBasic from "../../renderer" ;
import ApiDataFilter from "../../../../system/libraries/apiDataFilter" ;
/*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
创建一个渲染器实例
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
class Renderer extends AppRendererControllerBasic {
    constructor(req, res, next) {
        super(req, res, next) ;
        this.subEstateId = this.req.params.subEstateId || 81515 ;
        this.renders() ;
    }
    /*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
    渲染页面
    -----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
    async renders() {
        let modulePathArray = [ "xf" , "dynamic" ] ;   
        try {
            let adf = new ApiDataFilter(this.req.app) ;
            /*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
            获取api数据
            -----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
            let apiData = await adf.request({
                "apiPath" : modulePathArray.join(".") ,
                "method" : "post" ,
                "contentType" : "application/json" ,
                "data" : { "subEstateId" : this.subEstateId }
            }) ;
            let subEstateData = apiData.data ;
            /*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
            对城市的字段进行处理（去掉"市"字）
            -----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
            let shortName = decodeURIComponent(this.req.query.cityName) ; 
            let subEstateName = decodeURIComponent(this.req.query.subEstateName) ; 
            /*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
            扩展模板常规数据
            -----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/            
            Object.assign(this.templateData, { 
                "title" : subEstateName + "楼盘详情_" + shortName + subEstateName + "售楼处-悟空找房" , 
                "keywords" : subEstateName + "楼盘详情," + shortName + subEstateName + "售楼处" ,
                "description" : "悟空找房网为您提供" + shortName + subEstateName + "楼盘详情信息，在这里你可以知道" + subEstateName + "楼盘开发商，" + subEstateName + "售楼处，楼盘地址，物业，户型等详细介绍，了解更多关于" + subEstateName + "楼盘详情，就上悟空找房网。" ,                                
                "matchStylesheetPath" : modulePathArray.join("/") ,
                "controllerJavascriptPath" : modulePathArray.join("/") ,
                "items" : subEstateData
            }) ; 
            /*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
            渲染模板
            -----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/              
            this.render(modulePathArray.join("/")) ; 
        }
        catch(ex) {
            this.next(ex) ;
        }
    }
}
/*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
最后将render暴露出去
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
export default Renderer ;