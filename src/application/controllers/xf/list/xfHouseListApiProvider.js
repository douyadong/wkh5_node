import AppApiControllerBasic from "../../api" ;
import ApiDataFilter from "../../../../system/libraries/apiDataFilter" ;
import ParamGenerator from "./ParamGenerator";
/*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
创建一个apiProvider实例
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
class RestfulApi extends AppApiControllerBasic {
    constructor(req, res, next) {
        super(req, res, next) ;
        this.outputs() ;
    }
    /*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
    输出数据
    -----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
    async outputs() {
        try {
            let adf = new ApiDataFilter(this.req.app) ;
            let param = {
                cityId: 43,
                pageSize: 10
            };
                        
            if(this.req.body){
                param = new ParamGenerator({
                    pa: function(ret, data){
                        ret.offset = (data - 1) * 10;
                        ret.pageSize = 10;
                    },
                    of: function(ret, data){// 偏移量
                        ret.offset = data;
                        ret.pageSize = 10;
                    }
                }).getParamObj(ParamGenerator.normalize(this.req.body), param);
            }

            this.jsonObject = await adf.request({
                "apiPath" : "xf.list" ,
                "data" : param,
                "method":"post",
                "contentType":"application/json"
            }) ;

            if(this.jsonObject.data){// 为了pullload
                this.jsonObject.count = this.jsonObject.data.total;
            }else{
                this.jsonObject.count = 0;
            }

            /*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
            输出内容
            -----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
            this.output() ;
        }
        catch(ex){
            this.next(ex) ;
        }
        /*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
        结束
        -----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
    }
}

export default RestfulApi ;