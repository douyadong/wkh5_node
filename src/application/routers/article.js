/*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
加载相关资源
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
import express from "express" ;
import HybridArticleDetailRenderer from "../controllers/article/app-in/detail/renderer" ;
import ShareArticleDetailRenderer from "../controllers/article/app-share/detail/renderer" ;

import ArticleCommentListApiProvider from "../controllers/article/articleCommentListApiProvider" ;
import ArticleCommentApiProvider from "../controllers/article/articleCommentApiProvider" ;
let router = express.Router() ;
/*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
/article/app-in-xxx.html的路由规则
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
router.get("/article/app-in.(:articleId).html", function(req, res, next) {   
    new HybridArticleDetailRenderer(req, res, next) ;  
}) ;
/*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
/article/app-share-xxx.html的路由规则
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
router.get("/article/app-share.(:articleId).html", function(req, res, next) {   
    new ShareArticleDetailRenderer(req, res, next) ;  
}) ;
/*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
/api/article/articleCommentlist的路由规则
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
router.post("/api/article/articleCommentList", function(req, res, next) {   
    new ArticleCommentListApiProvider(req, res, next) ;  
}) ;
/*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
/api/article/articleComment的路由规则
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
router.post("/api/article/articleComment", function(req, res, next) {   
    new ArticleCommentApiProvider(req, res, next) ;  
}) ;

export default router ;
