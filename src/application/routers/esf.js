import express from "express" ;
import EsfDetailRenderer from "../controllers/esf/detail/renderer" ;
import EsfListRenderer from "../controllers/esf/list/renderer" ;
import EsfHouselistApiProvider from "../controllers/esf/list/houselistApiProvider" ;
let router = express.Router() ;
/*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
/shanghai/esf/12345.html的路由规则
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
router.get("/:city/esf/(:houseId).html", function(req, res, next) {   
    new EsfDetailRenderer(req, res, next) ;  
}) ;
/*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
/:city/esf的路由规则，没有condition
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
router.get("/:city/esf", function(req, res, next) {   
    new EsfListRenderer(req, res, next) ;  
}) ;
/*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
/:city/esf的路由规则，没有condition
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
router.get("/:city/esf/:condition", function(req, res, next) {   
    new EsfListRenderer(req, res, next) ;  
}) ;
/*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
/api/esf/houselist的路由规则
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
router.post("/api/esf/houselist", function(req, res, next) {   
    new EsfHouselistApiProvider(req, res, next) ;  
}) ;

export default router ;
